
public class $1_AreaTriangulo {
    public static void main(String[] args) {
        //b es el valor de la base del triangulo en cm
        double b = 5;
        //h es el valor de la altura del triangulo en cm
        double h = 10;
        //calculo del area de un triangulo en cm
        double area = (b*h)/2;
        //imprimiendo el resultado del area
        System.out.print("El area del triangulo es = " + area + " cm^2");
    }
}
